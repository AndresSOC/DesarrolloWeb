// Función 1: sumar(a, b)
// Debe retornar la suma de a y b
// export function sumar ...
// ....

export function sumar(a,b){
    return(a+b);
}

// Función 2: multiplicar(a, b)
// Debe retornar el producto de a por b
// export function multiplicar ...
// ....
export function multiplicar(a,b){
    return(a * b);
}


// Función 3: cuadrado(n)
// Debe retornar n elevado al cuadrado (n * n)
// export function cuadrado ...
// ....
export function cuadrado(n){
    return n * n;
}
