// Declare un arreglo vacío
let arreglo = [];

// 1. Agregue 10 al final con push
// ...
arreglo.push(10);
// Imprima: push(10): [elementos]
// ...
console.log(arreglo);

// 2. Agregue 20 al final con push
// ...
arreglo.push(20);

// Imprima: push(20): [elementos]
// ...
console.log(arreglo);

// 3. Agregue 5 al inicio con unshift
// ...
arreglo.unshift(5);
// Imprima: unshift(5): [elementos]
// ...
console.log(arreglo);

// 4. Agregue 1 al inicio con unshift
// ...
arreglo.unshift(1);
// Imprima: unshift(1): [elementos]
// ...
console.log(arreglo)

// 5. Elimine el último elemento con pop
// ...
arreglo.pop()

// Imprima: pop(): [elementos]
// ...
console.log(arreglo)

// 6. Elimine el primer elemento con shift
// ...

arreglo.shift()

// Imprima: shift(): [elementos]
// ...
console.log(arreglo)